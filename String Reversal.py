def reverse_string(input_str):
    """
    Reverses the input string and returns the result.

    Args:
        input_str (str): The string to be reversed

    Returns:
        str: The reversed string
    """
    return input_str[::-1]

print(reverse_string("hii"))  # Output: "iih"
print(reverse_string("coder"))  # Output: "redoc"
print(reverse_string("aditya"))  # Output: "erte"